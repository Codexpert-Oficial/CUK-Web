const modalContainer = document.querySelector(".modal__container");
const modal = document.querySelector(".modal");
const modalImg = document.querySelector(".modal__img");
const modalDescription = document.querySelector(".modal__description");
const modalIconClose = document.querySelector(".modal__close__icon");

modalContainer.addEventListener("click", () => {
    modalContainer.classList.remove("modal__container-active")
});

modalIconClose.addEventListener("click", () => {
    modalContainer.classList.remove("modal__container-active")
});

modal.addEventListener("click", (e) => e.stopPropagation())

const changeModalData = (organizationName) => {
    const organization = data[organizationName];

    if (organization) {
        modalDescription.innerHTML = "";
        for (const property in organization) {
            if (organization.hasOwnProperty(property)) {
                modalDescription.innerHTML += `<p> ${property}: ${organization[property]} </p>`;
                if (document.querySelector(".events")) { // este if hace que si esta en el index tenga una ruta, y si esta en affiliates.html tenga otra
                    modalImg.src = `./imgs/logos/${organizationName}.png`;
                } else {
                    modalImg.src = `../imgs/logos/${organizationName}.png`;
                }
            }
        }
    }
}

const affiliatesElements = document.querySelectorAll(".affiliates__element");
const affiliatesElementsTitles = document.querySelectorAll(".affiliates__element__title");

affiliatesElements.forEach((element, index) => {
    element.addEventListener("click", () => {
        modalContainer.classList.add("modal__container-active")
        changeModalData(affiliatesElementsTitles[index].textContent)
    })
});



