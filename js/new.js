const urlParams = new URLSearchParams(window.location.search);
const newIndex = urlParams.get('id');

const seeNew = (index) => {
    const newTitle = document.querySelector(".new__title");
    const newDescription = document.querySelector(".new__description");
    const newImagesContainer = document.querySelector(".new__imgs__container");

    const newData = news[index];

    newTitle.textContent = newData.title;

    newData.texts.forEach(text => {
        const p = document.createElement("p");
        p.textContent = text;
        newDescription.appendChild(p);
    });

    newData.images.forEach(imageURL => {
        const img = document.createElement("img");
        img.classList.add("new__img")
        img.src = imageURL;
        newImagesContainer.appendChild(img);
    });

}

seeNew(newIndex);

